// Files renders the full, buildable basekit project for f as an in-memory
// map of relative-path → content. Scaffold and Zip both build on this so disk
// and archive output never drift.
func Files(f FieldShortcut) (map[string]string, error) {
	index, err := RenderIndexTS(f)
	if err != nil {
		return nil, err
	}
	return map[string]string{
		"package.json":  RenderPackageJSON(f),
		"tsconfig.json": tsconfigJSON,
		"config.json":   "{\n  \"authorizations\": []\n}\n",
		"src/index.ts":  index,
		"test/index.ts": RenderTestTS(f),
		"README.md": fmt.Sprintf("# %s\n\nGenerated field shortcut (basekit), human-auditable source.\n\n"+
			"## Build & upload\n\n```\nnpm install\nnpm run build   # type-check\nnpm run pack    # -> output/*.zip\n```\n\n"+
			"Then upload the zip to your Feishu developer console (field-shortcut capability) and have an admin approve it.\n", f.Title.ZhCN),
	}, nil
}

// sortedKeys returns the file paths in deterministic order.
func sortedKeys(files map[string]string) []string {
	keys := make([]string, 0, len(files))
	for k := range files {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

// Scaffold writes a full, buildable basekit project for f under dir.
func Scaffold(f FieldShortcut, dir string) error {
	files, err := Files(f)
	if err != nil {
		return err
	}
	for _, rel := range sortedKeys(files) {
		p := filepath.Join(dir, rel)
		if err := os.MkdirAll(filepath.Dir(p), 0o755); err != nil {
			return err
		}
		if err := os.WriteFile(p, []byte(files[rel]), 0o644); err != nil {
			return err
		}
	}
	return nil
}

// Zip renders the project and returns it as a .zip archive (the downloadable
// deliverable for the web platform).
func Zip(f FieldShortcut) ([]byte, error) {
	files, err := Files(f)
	if err != nil {
		return nil, err
	}
	var buf bytes.Buffer
	zw := zip.NewWriter(&buf)
	for _, rel := range sortedKeys(files) {
		fw, err := zw.Create(rel)
		if err != nil {
			return nil, err
		}
		if _, err := fw.Write([]byte(files[rel])); err != nil {
			return nil, err
		}
	}
	if err := zw.Close(); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}
